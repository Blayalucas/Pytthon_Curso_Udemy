# operador lógico "not"
# Usando para inverter expressões 
# not True = False 
# not False = True

senha = input("senha:  ")

if not senha:
    print("Você não digitou a senha")

# Outro exemplo

print (not True)   # O resultado dessa expressão o resultado será Falso 
print (not False)  # essa expressão o resultado será verdadeira 
                   # ele "inverte"