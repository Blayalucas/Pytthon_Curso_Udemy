# __eq__(self,other) - self == other (se é igual )
# __ne__(self,other) - self != other (Se é diferente)
# __add__(self,other) - self + other ( se quiser adicionar um objeto)
# __sub__(self,other) - self - other ( se quiser subtrair um objeto)
# __mul__(self,other) - self * other ( se quiser multiplicar um objeto)
