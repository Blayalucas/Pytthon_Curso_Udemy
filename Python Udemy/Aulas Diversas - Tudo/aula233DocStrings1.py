# DocStrings __DOC__ - help
"""
Docstrings __DOCT__ajudam você a entender os recursos de um módulo ou função. 
Por exemplo, digamos que você fez um Def e comentou se você chamar a função
mais o __doc__ ele mostra o comentário 
basta usar :

help(funçao.__doc__)

"""
def square(n):
    """Takes in a number n, returns the square of n"""
    return n**2

help(square.__doc__)

# Isso Ajuda outros programadores a entender o processo e o seu codigo
# atraves do comentário 
