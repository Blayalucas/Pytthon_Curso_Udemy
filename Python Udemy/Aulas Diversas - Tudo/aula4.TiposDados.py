# Tipos de Numeros 
# Tipos int e float 
# int-> Numero inteiro 
# O tipo int representa qualquer número
# positivo ou negativo. int sem sinal é considerado 
# positivo. 
print(11) # int
print(-11) #int
print(0) # int

# float → Número com ponto flutuantes 
# O tipo float representa qualquer número 
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo. 
# Ponto é a virgula para números - 2.1 2.2 3.5
# A virgula serve para separar apenas 
print (1.1, 3.5, 8.5)
print (0.0,60.5)

# A função type mostra o tipo que o Python 
# inferiu  ao  valor
# Mostra que Lucas é uma string - str
print(type('Lucas'))
# Mostra que 1 é um número  inteiro - int
print(type(1))
# Mostra os tipos de float - numeros com ponto - exemplo 3,5 8,9...
print(type(1.1), type(-1.1), type(0.0))
