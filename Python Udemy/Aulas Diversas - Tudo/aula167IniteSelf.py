# init e Self

"""
Dentro da DEF o _init_ é o "inicio' e o Self é o parâmetro.

OBS : Ambos é OBRIGATORIO

Passo a Passo para criar uma classes perfeita 

Segue a estrutura SEMPRE


"""

# Segundo Passo 
class Carro:
    def __init__(self, nome):
        self.nome = nome


# Primeiro Passo 
fusca = Carro ("Fusca")

# Terceiro passo
print(fusca.nome)


